#CREAR UN ALGORITMO QUE SIRVA DE CONVERSOR DE TEMPERATURA

celsius = float(input('Ingrese la temperatura en grados Celsius: \n'))
kevin = celsius + 273.15 
fahrenheit = (celsius * 9/5) + 32

print(f'Los grados en Celsius: {celsius} °C, en Kevin equivalen a : {kevin: .2f} K, y en Fahrenheit equivalen : {fahrenheit: .2f} °F')